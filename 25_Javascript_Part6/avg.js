// Function to calculate the average of three numbers

function avg(num1, num2, num3){
    let res
    res = (num1+num2+num3)/3
    return res.toFixed(2)
}

console.log(avg(1,2,3))
console.log(avg(2,4,6))
console.log(avg(2,4,8))


